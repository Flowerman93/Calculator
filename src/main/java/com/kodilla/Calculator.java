package com.kodilla;

public class Calculator {
    float firstNumber;
    float secondNumber;

    public Calculator(){
        this.firstNumber = firstNumber;
        this.secondNumber = secondNumber;
    }

    public void addition(double firstNumber, double secondNumber){
        double result;
        result = firstNumber + secondNumber;
        System.out.println("Addition: " + firstNumber + " + " + secondNumber + " = " + result);
    }

    public void subtraction(double firstNumber, double secondNumber){
        double result;
        result = firstNumber - secondNumber;
        System.out.println("Subtraction: " + firstNumber + " - " + secondNumber + " = " + result);
    }
}
